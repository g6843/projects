package web;

public class ImageObject {
			
		String name;
		String url; 
		
		public ImageObject(String name, String url){
			this.name = name; 
			this.url = url;
		}
		
		public void setname(){
			this.name = name;
		}
		
		public String getname(){
			return name;
		}
		
		public String geturl(){
			return url;
		}
		
		public void seturl(){
			this.url = url;
		}
}
